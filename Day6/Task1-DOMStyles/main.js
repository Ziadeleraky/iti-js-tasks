let paragraph = document.getElementById("PAR").style;
function ChangeFont(fontName) {
  paragraph.fontFamily = fontName;
}

function ChangeAlign(align) {
  paragraph.textAlign = align;
}

function ChangeHeight(height) {
  paragraph.lineHeight = height;
}

function ChangeLSpace(space) {
  paragraph.letterSpacing = space;
}

function ChangeIndent(indent) {
  paragraph.textIndent = indent;
}

function ChangeTransform(transform) {
  paragraph.textTransform = transform;
}

function ChangeDecorate(decorate) {
  paragraph.textDecoration = decorate;
}

function ChangeBorder(border) {
  paragraph.borderStyle = border;
}

function ChangeBorderColor(color) {
  paragraph.borderColor = color;
}
